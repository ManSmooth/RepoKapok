package DogClass;
public class Dog {
    private String color;
    private double weight;

    public Dog(String color, double weight){
        this.color = color;
        this.weight = weight;
    }

    public String getColor(){
        return this.color;
    }

    public double getWeight(){
        return this.weight;
    }


}